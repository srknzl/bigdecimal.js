describe('Half-way rounding at the representation boundary', function () {

    it('matches Java for divisors and remainders around the half point', function () {
        for (const [n, d, mode, expected] of JAVA_CASES) {
            Big(n).divide(Big(d), 0, RoundingMode[mode]).toString()
                .should.equal(expected, `${n} / ${d} ${mode}`);
        }
    });

    // Negating MAX_SAFE_INTEGER yields MIN_SAFE_INTEGER, which is the INFLATED
    // sentinel, so negate().negate() returns an equal value that is forced onto the
    // inflated path. Any operation must not be able to tell the two apart.
    it('gives identical results for compact and inflated forms of an equal value', function () {
        const M = Number.MAX_SAFE_INTEGER;
        const compact = Big(M);
        const inflated = compact.negate().negate();
        compact.equals(inflated).should.equal(true);

        const dividends = [
            '13510798882111485', '13510798882111486', '13510798882111487',
            '4503599627370495', '4503599627370496', '9007199254740991',
            '27021597764222972', '27021597764222973',
        ];
        for (const n of dividends) {
            for (const mode of ['HALF_UP', 'HALF_DOWN', 'HALF_EVEN', 'UP', 'DOWN', 'CEILING', 'FLOOR']) {
                for (const sign of [1, -1]) {
                    const dividend = sign < 0 ? Big(n).negate() : Big(n);
                    const viaCompact = dividend.divide(compact, 0, RoundingMode[mode]).toString();
                    const viaInflated = dividend.divide(inflated, 0, RoundingMode[mode]).toString();
                    viaInflated.should.equal(viaCompact, `${sign * 1} ${n} / MAX_SAFE_INTEGER ${mode}`);
                }
            }
        }
    });

    it('still detects an exact tie when the divisor is even', function () {
        Big(3).divide(Big(2), 0, RoundingMode.HALF_UP).toString().should.equal('2');
        Big(3).divide(Big(2), 0, RoundingMode.HALF_DOWN).toString().should.equal('1');
        Big(3).divide(Big(2), 0, RoundingMode.HALF_EVEN).toString().should.equal('2');
        Big(5).divide(Big(2), 0, RoundingMode.HALF_EVEN).toString().should.equal('2');
        // exact tie on a clearly inflated even divisor
        Big('61728394506172839451').divide(Big('123456789012345678902'), 0, RoundingMode.HALF_EVEN)
            .toString().should.equal('0');
        Big('61728394506172839451').divide(Big('123456789012345678902'), 0, RoundingMode.HALF_UP)
            .toString().should.equal('1');
    });
});
